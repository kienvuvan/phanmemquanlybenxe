    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        // TODO add your handling code here:
        if (jComboBox_bsxNoSort.getSelectedItem() == null || jComboBox_viTriDoXe.getSelectedItem() == null || jComboBox_thoiGianDo.getSelectedItem() == null) {
            JOptionPane.showMessageDialog(this, "Hiện tại không có xe cần sắp xếp vào trong bãi");
        } else {
            String bsx = jComboBox_bsxNoSort.getSelectedItem().toString();
            String viTriDo = jComboBox_viTriDoXe.getSelectedItem().toString();
            String thoiGianDo = jComboBox_thoiGianDo.getSelectedItem().toString();
            Car car = new Car(bsx, viTriDo, thoiGianDo);
            int result = carController.sortCarInPark(car);
            switch (result) {
                case MysqlCarDao.RESULT_EMPTY:
                    JOptionPane.showMessageDialog(this, "Các trường dữ liệu không được để trống.Vui lòng điền đầy đủ");
                    break;
                case MysqlCarDao.RESULT_PARKED:
                    JOptionPane.showMessageDialog(this, "Vị trí này đã có xe đỗ trong khoảng thời gian " + thoiGianDo);
                    break;
                case MysqlCarDao.RESULT_SUCCESS:
                    JOptionPane.showMessageDialog(this, "Sắp xếp xe thành công");
                    setVisiableManagerParkCar();
                    break;
                case MysqlCarDao.RESULT_ERROR_SQL:
                    JOptionPane.showMessageDialog(this, "Đã có lỗi xảy ra.Vui lòng thử lại sau");
                    break;
            }
        }
    }//GEN-LAST:event_jButton7ActionPerformed

